class DataWithCollectsite:
    def __init__(self):
        self.data = None
        self.collect_site = None

    def get_data(self):
        return self.data

    def set_data(self, data):
        self.data = data

    def get_collect_site(self):
        return self.collect_site

    def set_collect_site(self, collect_site):
        self.collect_site = collect_site

